public class PairInt {
	private int x;
	private int y;
	
	
	public PairInt(int v1, int v2) {
		x = v1;
		y = v2;
	}
	
	public PairInt() {
		x = 0;
		y = 0;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
	public void setX(int xp) {
		x = xp;
	}

	public void setY(int yp) {
		y = yp;
	}
}
